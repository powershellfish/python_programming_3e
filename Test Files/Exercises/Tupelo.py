# Tuplelo Tuple

# creates an empty tuple
tupelo = ()

# string values to the tuple
tupelo = ("honey",
          "steak",
          "wine",
          "wings")

# prints the tuple as is
print("Here is the tuple as-is:")
print(tupelo)

# prints each individual element of the tuple

print("\n")
print("Here is each element in the tuple printed out:")
for item in tupelo:
    print(item)

# This for loop can be used to run through elements of *any* sequence
